                <p class="colorbox-text">Cardiologia Congênita</p>
                <p class="exame_descricao">A cardiologia congênita é a especialidade que cuida das doenças do coração que já existem ao nascimento (congênito), sendo responsável pelo seu seguimento desde o nascimento até a fase adulta da vida.</p><br>
                <a class="agendar" href="../PGMS/exames.php">Agendar</a>
            </div>
            <div class="colorbox">
                <p class="colorbox-text">Tomografia</p>
                <p class="exame_descricao">A tomografia computorizada (TC) cardíaca é um exame que serve para auxiliar o Médico Cardiologista (especialista em doenças do coração) no diagnóstico e resposta aos tratamentos em diversas patologias (doenças). </p><br>
                <a class="agendar" href="../PGMS/exames.php">Agendar</a>
            </div>
