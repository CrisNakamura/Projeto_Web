                <p class="exame_descricao">O Holter de 24 horas é um exame semelhante ao MAPA, mas neste caso o aparelho fica conectado a eletrodos no tórax do(a) paciente que monitoram o coração por 24h. A intenção do exame é identificar arritmias, palpitações e investigar o motivo de tonturas e desmaios.</p>
                <a class="agendar" href="../PGMS/exames.php">Agendar</a>
            </div>
